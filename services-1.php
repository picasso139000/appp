        <h3>1.1 TYPES OF RIG INSPECTIONS</h3>
        <p>
          Rig inspections generally fall into several categories based on their scope and frequency. Common types of rig inspections include:
        </p>


        <ul>
          <li><i class="bi bi-check-circle"></i> <span>1. Pre-Operational Inspections:</span></li>
          <p>
            Conducted before a rig is mobilized for a new job or begins operation, these inspections verify that all equipment and systems are fully operational and meet safety standards. This inspection is essential for ensuring the rig is fit for purpose and reducing the likelihood of operational failures during drilling. </p> <br>
          <img src="assets/img/services/Pre-Operational Inspections.png" style="width:100px" alt="">
          <img src="assets/img/services/Pre-Operational Inspections2.png" style="width:100px" alt=""><br><br>
          <li><i class="bi bi-check-circle"></i> <span>2. Routine Operational Inspections:</span></li>
          <p> These inspections are scheduled on a regular basis (e.g., weekly, monthly, quarterly) to monitor the ongoing condition of equipment and identify any signs of wear, damage, or deterioration. Routine inspections are essential for ensuring that safety systems, critical infrastructure, and operational equipment remain in optimal condition.</p>
          <img src="assets/img/services/Routine.png" style="width:100px" alt=""><br><br>

          <li><i class="bi bi-check-circle"></i> <span>3. Annual Inspections:</span></li>
          <p> More comprehensive than routine inspections, annual inspections typically involve a detailed review of the rig’s safety, mechanical, and electrical systems, as well as structural integrity. These inspections may require a team of inspectors and specialists to conduct thorough assessments of critical components, such as the blowout preventer (BOP), lifeboats, cranes, and pipelines.</p>
          <img src="assets/img/services/Annual.png" style="width:100px" alt=""><br><br>

          <li><i class="bi bi-check-circle"></i> <span>4. Regulatory Compliance Inspections:</span></li>
          <p> Regulatory bodies, such as the U.S. Bureau of Safety and Environmental Enforcement (BSEE), the International Maritime Organization (IMO), and other national or regional authorities, require specific inspections to ensure compliance with industry standards, environmental regulations, and safety protocols. These inspections are mandatory and may result in certifications that allow rigs to operate legally.</p>
          <img src="assets/img/services/Regulatory.png" style="width:100px" alt=""><br><br>

          <li><i class="bi bi-check-circle"></i> <span>5. Post-Incident Inspections:</span></li>
          <p> After any incident, accident, or near-miss, a thorough investigation and inspection are conducted to assess damage, determine root causes, and recommend corrective actions to prevent future occurrences. This is a reactive form of inspection triggered by an event that raises concerns about equipment integrity or safety.</p>
          <img src="assets/img/services/Post.png" style="width:100px" alt=""><br><br>

          <li><i class="bi bi-check-circle"></i> <span>6. Specialized Inspections:</span></li>
          <p> These inspections focus on specific equipment or systems, such as non-destructive testing (NDT) of structural materials, inspections of safety barriers and fire suppression systems, or testing of critical components like pressure relief valves, generators, and control panels.</p>
          <img src="assets/img/services/Specialized.png" style="width:100px" alt=""><br><br>

        </ul>
