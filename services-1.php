        <h3>1.2 COMPONENTS OF RIG INSPECTIONS:</h3>

        <p>
          Rig inspection services typically focus on several core areas:
        </p>

        <ul>
          <li><i class="bi bi-check-circle"></i> <span>1. Structural Integrity</span></li>
          <p>
            Inspectors assess the rig’s structural components, including the derrick, legs, jack-up systems, and subsea structures (for offshore rigs). Any signs of corrosion, cracking, or deformation are documented and prioritized for repair or maintenance.
          </p>
          <img src="assets/img/services/Structural Integrity.png" style="width:100px" alt=""><br><br>


          <li><i class="bi bi-check-circle"></i> <span>2. Mechanical and Electrical Systems:</span></li>
          <p>This includes checks on engines, generators, pumps, motors, hydraulic systems, and electrical circuits. Proper functioning of mechanical components ensures the rig can operate efficiently, while electrical inspections are crucial for preventing fires, explosions, and system failures.</p>
          <img src="assets/img/services/Mechanical and Electrical Systems.png" style="width:100px" alt=""><br><br>

          <li><i class="bi bi-check-circle"></i> <span>3. Safety Systems and Equipment:</span></li>
          <p> Safety systems, such as blowout preventers (BOPs), fire suppression systems, lifeboats, life rafts, personal protective equipment (PPE), emergency evacuation routes, and alarms, are tested for compliance with safety regulations and operational readiness.</p>

          <li><i class="bi bi-check-circle"></i> <span>4. Environmental Compliance:</span></li>
          <p>Inspections ensure that the rig’s operations comply with environmental protection regulations, particularly in offshore operations. This includes evaluating waste management systems, spill prevention measures, and emissions control systems.</p>
          <img src="assets/img/services/Environmental Compliance.png" style="width:100px" alt=""><br><br>

          <li><i class="bi bi-check-circle"></i> <span>5. Operational and Control Systems:</span></li>
          <p> A critical part of the inspection process is assessing the rig’s operational control systems, including its monitoring and diagnostic systems, communication equipment, and automation features. These systems are essential for controlling the rig’s operations, ensuring safety, and maintaining data integrity.</p>
          <img src="assets/img/services/Operational and Control Systems.png" style="width:100px" alt=""><br><br>

        </ul>
